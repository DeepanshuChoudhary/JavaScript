'use strict';

// For Loop
for(let i=0; i<=10; i++) {
    // console.log("hello " + i);
    // console.log("2 * " +i+ " = " + 2*i);

    console.log(`2 x ${i} = ${2*i}`);
}

console.log("For, Completed!!!");

// While Loop
var a = 1;
while(a<=10) {
    console.log(`5 x ${a} = ${5*a}`);
    a++;
}

console.log("While Completed");


// Do-While Loop
var b = 1;
do {
    console.log(`4 x ${b} = ${4*b}`);
    b++;
} while(b<=10)

console.log("Do-While Completed!!!");